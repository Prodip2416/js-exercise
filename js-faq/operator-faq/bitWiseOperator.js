var a = 6; 
var b = 1; 
  
console.log("A & B = " + (a & b)); 
  
console.log("A | B = " + (a | b)); 

console.log("~A = " + (~a)); 
  
console.log("A >> B = " + (a >> b)); 
  
console.log("A >>> B = " + (a >>> b)); 
  
console.log("A << B = " + (a << b));  

// Result
// A & B = 0
// A | B = 7
// ~A = -7
// A >> B = 3
// A >>> B = 3
// A << B = 12